package weekend.tasks;

public class MathFuctions {

		public static void main(String[] args){
			System.out.println(Math.abs(-26.7)); //Absolute takes any number and gives it opposite value
			
			System.out.println(Math.ceil(7.4)); //Always rounds higher
			
			System.out.println(Math.floor(7.8)); //Always rounds lower
			
			System.out.println(Math.max(8.6,9.2)); //Finds the maximum / largest of the two numbers
			
			System.out.println(Math.min(5.3, 8.2)); //Finds the minimum / smallest of the two numbers
			
			System.out.println(Math.pow(5, 3)); //5 to the power of 3
			
			System.out.println(Math.sqrt(9)); //Square root of 9
		}

	}